#List Methods in python(len(),max()&min())
a=["ram",10,"shyam",12.5]
x=["ram","shyam","Shyam","Tapina","Bapi","qwertyk"]
y=[2,5,2,6,9,15,59]

#len()
b=len(a)
print("Length of the list(a)=",b)
#or
print("Length of the list(x)=",len(x))
print("Length of the list(y)=",len(y))
#max()
z=max(y)
print("Max in the list(y):",z)
#or
print("Max in the list(y):",max(y))

z1=max(x)
print("Max in the list(x):",z1)
#or
print("Max in the list(x):",max(x))

#min()
z2=min(x)
print("Min in the list(x):",z2)
#or
print("Min in the list(x):",min(x))


